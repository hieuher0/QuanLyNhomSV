    private void showData() {
        modelHW.setRowCount(0);
        for (HomeWork hw : listHW) {
            modelHW.addRow(new Object[]{
                hw.getId(), hw.getName(), hw.getType(), hw.getTotalTime()
            });
        }
    }
    private void showHomeWork() {
        listHW = controller.readDataFromFile("BT.DAT");
        if (listHW.size() > 0) {
            int id = listHW.get(listHW.size() - 1).getId();
            HomeWork.setsId(id + 1);
        }
        showData();
    }
