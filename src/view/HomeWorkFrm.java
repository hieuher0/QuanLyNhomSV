    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int index = tabHomeWork.getSelectedRow();
        if(index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn bài tập để xóa");
        } else {
            asf.deleteLinkToAssignment(listHW.get(index));
            listHW.remove(index);
            controller.deleteFile("BT.DAT");
            controller.writeToFile(listHW, "BT.DAT");
            showHomeWork();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

