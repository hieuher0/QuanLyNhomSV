    private void comboSortStuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboSortStuActionPerformed
        // TODO add your handling code here:
        int index = comboSortStu.getSelectedIndex();
        if(index == 0) {
            controller.sortByName(listStu);
        } else if(index == 1) {
            controller.sortByAddress(listStu);
        } else if(index == 2) {
            controller.sortByClass(listStu);
        }
        showData();
    }//GEN-LAST:event_comboSortStuActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int index = tabStudent.getSelectedRow();
        if(index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn sinh viên để xóa");
        } else {
            
            asf.deleteLinkToAssignment(listStu.get(index));
            listStu.remove(index);
            controller.deleteFile("SV.DAT");
            controller.writeToFile(listStu, "SV.DAT");
            showData();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

