    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id = 0;
        try {
            id = Integer.parseInt(txtId.getText());
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Mã sinh viên là số");
            return;
        }
        for (Student student : listStu) {
            if(student.getId() == id) {
                Student st = student;
                listStu.clear();
                listStu.add(st);
                showData();
                break;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int index = tabStudent.getSelectedRow();
        if(index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn sinh viên để đặt lại mật khẩu");
            return;
        }
        Student st = listStu.get(index);
        dstk.addAll(controller.readDataFromFile("TKSV.txt"));
        for (TaiKhoan taiKhoan : dstk) {
            if(taiKhoan.getId().equals(""+st.getId())) {
                taiKhoan.setMatkhau("123456");
                controller.deleteFile("TKSV.txt");
                controller.writeToFile(dstk, "TKSV.txt");
                JOptionPane.showMessageDialog(rootPane, "Đặt lại mật khẩu thành công");
                break;
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

