    private void showData() {
        modelStu.setRowCount(0);
        for (Student student : listStu) {
            modelStu.addRow(new Object[]{
                student.getId(), student.getTen(), student.getAddress(), student.getLop()
            });
        }
    }
    
    private void showStudent() {
        listStu = controller.readDataFromFile("SV.DAT");
        if(listStu.size() > 0) {
            int id = listStu.get(listStu.size()-1).getId();
            Student.setsId(id+1);
        }
        showData();
    }
